package org.example.domain.model.enums;

/**
 * Типы результатов перемещения и действий. Используется в MoveResult для классификации
 * произошедшего события.
 */
public enum MoveResultType {

    /** Обычное перемещение на пустую клетку. */
    MOVE,

    /** Перемещение заблокировано (стена, граница карты). */
    BLOCKED,

    /** Подобран предмет с пола. */
    PICKUP,

    /** Атака попала, монстр получил урон но жив. */
    ATTACK_HIT,

    /** Атака убила монстра. */
    ATTACK_KILL,

    /** Переход на следующий уровень (игрок наступил на выход). */
    NEXT_LEVEL,

    /** Игрок использовал предмет из инвентаря. */
    ITEM_USED,

    /** Игрок экипировал оружие. */
    WEAPON_EQUIPPED,

    /** Игрок снял оружие. */
    WEAPON_UNEQUIPPED,

    /** Игрок пропустил ход (wait). */
    WAIT,

    /** Дверь открыта ключом. */
    DOOR_OPENED,

    /** Дверь заперта, нет ключа. */
    DOOR_LOCKED,

    /** Найден ключ. */
    KEY_FOUND,

    /** Монстр переместился. */
    MONSTER_MOVE,

    /** Монстр атаковал игрока. */
    MONSTER_ATTACK,

    /** Монстр не может двигаться (заблокирован). */
    MONSTER_BLOCKED,

    /** Монстр телепортировался (для призрака). */
    MONSTER_TELEPORTED,

    /** Монстр стал невидимым (для призрака). */
    MONSTER_INVISIBLE,

    /** Монстр отдыхает (для огра). */
    MONSTER_RESTING,

    /** Монстр стоит на месте */
    MONSTER_WAIT,

    /** Игрок усыплен (змеем-магом). */
    PLAYER_ASLEEP,

    /** Игрок проснулся. */
    PLAYER_WOKE_UP,

    /** Временный эффект применен (эликсир). */
    BUFF_APPLIED,

    /** Временный эффект закончился. */
    BUFF_EXPIRED,

    /** Вампир восстановил здоровье. */
    VAMPIRE_HEALED,

    /** Огр контратаковал. */
    OGRE_COUNTER_ATTACK,

    /** Статистика обновлена. */
    STATS_UPDATED,

    /** Достижение разблокировано. */
    ACHIEVEMENT_UNLOCKED,

    /** Требуется отображение сообщения. */
    MESSAGE,

    /** Требуется обновление интерфейса. */
    UI_UPDATE,

    /** Требуется перерисовка всего экрана. */
    FULL_REDRAW;
}
