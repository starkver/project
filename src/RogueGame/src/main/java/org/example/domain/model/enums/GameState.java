package org.example.domain.model.enums;

/** Состояния игровой сессии. Определяет, в каком режиме находится игра в данный момент. */
public enum GameState {

    /**
     * Активная игра - игрок жив и проходит подземелье. Основное состояние, в котором происходит
     * игровой цикл.
     */
    PLAYING,

    /** Игрок погиб. Показывается экран смерти, после чего можно начать новую игру или выйти. */
    GAME_OVER,

    /**
     * Игра пройдена - игрок успешно завершил 21 уровень. Показывается экран победы с финальной
     * статистикой.
     */
    VICTORY,

    /** Пауза - игра приостановлена. Может использоваться для меню паузы. */
    PAUSED,

    /**
     * Переход между уровнями. Короткое состояние, когда старый уровень выгружается, а новый
     * генерируется.
     */
    LEVEL_TRANSITION,

    /** Режим просмотра инвентаря. Игрок открыл рюкзак и выбирает предметы. */
    INVENTORY,

    /**
     * Режим выбора предмета для использования. Показывает список предметов определенного типа (1-9).
     */
    ITEM_SELECTION,

    /** Режим просмотра статистики текущей игры. */
    STATS_SCREEN,

    /** Игрок сохранил игру и вышел. Состояние для корректного завершения. */
    SAVED_AND_QUIT,

    /** Режим ожидания - игрок пропускает ход. Используется, когда игрок выбрал WAIT. */
    WAITING,

    /**
     * Режим сна - игрок усыплен змеем-магом. Игрок не может двигаться, но может проснуться через ход.
     * Отличается от WAITING тем, что это вынужденное состояние, а не добровольный пропуск хода.
     */
    SLEEPING,

    /** Режим просмотра таблицы рекордов. */
    LEADERBOARD,

    /** Режим подтверждения (например, выход без сохранения). */
    CONFIRMATION;

    /**
     * Получить понятное пользователю название состояния.
     *
     * @return строковое представление для пользователя
     */
    public String getUserFriendlyName() {
        switch (this) {
            case PLAYING:
                return "В игре";
            case GAME_OVER:
                return "Игра окончена";
            case VICTORY:
                return "Победа!";
            case PAUSED:
                return "Пауза";
            case LEVEL_TRANSITION:
                return "Переход на уровень";
            case INVENTORY:
                return "Инвентарь";
            case ITEM_SELECTION:
                return "Выбор предмета";
            case STATS_SCREEN:
                return "Статистика";
            case SAVED_AND_QUIT:
                return "Сохранено";
            case WAITING:
                return "Ожидание";
            case SLEEPING:
                return "Сон";
            case LEADERBOARD:
                return "Таблица рекордов";
            case CONFIRMATION:
                return "Подтверждение";
            default:
                return this.name();
        }
    }
}
